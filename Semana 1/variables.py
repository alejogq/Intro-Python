
"""
Entradas
"""

kilosPapa = float( input("Ingrese Kilos de papa (2000 el kilo): ") )

kilosTomate = float( input("Ingrese Kilos de tomate (2500 el kilo): ") )


"""
Procedimiento
"""
Total = kilosPapa*2000 + kilosTomate*2500

"""
Entrego al usuario
"""
print("El costo total es: ", Total)

print("El costo preciso es:  %.2f "%Total)






